//   material
// );
// box.position.set(figureMap.box.pos.x, figureMap.box.pos.y, figureMap.box.pos.z);
// box.castShadow = true;
// box.receiveShadow = true;

const plane = new THREE.Mesh(
  new THREE[figureMap.plane.geometry.type](...figureMap.plane.geometry.args),
  material
);
plane.rotation.x = figureMap.plane.rot.x;
plane.position.y = figureMap.plane.pos.y;

plane.receiveShadow = true;

scene.add(plane);

/**
 * Constructive Solid Geometry Tests
*/
// let meshA = new THREE.Mesh(new THREE.BoxGeometry(1,1,1), material);
// let meshB = new THREE.Mesh(new THREE.BoxGeometry(1,1,1), material);

const meshA = new THREE.Mesh(
  new THREE[figureMap.box.geometry.type](...figureMap.box.geometry.args),
  material
);
const meshB = new THREE.Mesh(
  new THREE[figureMap.box.geometry.type](...figureMap.box.geometry.args),
  material
);

meshB.position.add(new THREE.Vector3( 0.5, 0.5, 0.5));

meshA.updateMatrix();
meshB.updateMatrix();

let bspA = CSG.fromMesh( meshA );
let bspB = CSG.fromMesh( meshB );

let bspResult = bspA.union(bspB);
let meshResult = CSG.toMesh( bspResult, meshA.matrix, meshA.material );

scene.add(meshResult); // <---- anaglyphic does this work for anaver.se integration?

/**
 * Sizes
 */
const sizes = {
  width: window.innerWidth,
  height: window.innerHeight,
};

window.addEventListener("resize", () => {
  // Update sizes
  sizes.width = window.innerWidth;
  sizes.height = window.innerHeight;

  // Update camera
  camera.aspect = sizes.width / sizes.height;
  camera.updateProjectionMatrix();

  // Update renderer
  renderer.setSize(sizes.width, sizes.height);
  renderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));
});

/**
 * Camera
 */
// Base camera
const camera = new THREE.PerspectiveCamera(
  75,
  sizes.width / sizes.height,
  0.1,
  100
);
camera.position.x = 1;
camera.position.y = 1;
camera.position.z = 2;
scene.add(camera);

// Controls
const controls = new OrbitControls(camera, canvas);
controls.enableDamping = true;

/**
 * Renderer
 */
const renderer = new THREE.WebGLRenderer({
  canvas: canvas,
});
renderer.setSize(sizes.width, sizes.height);
renderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));
renderer.shadowMap.enabled = true; // enable for shadows ;)
renderer.shadowMap.type = THREE.PCFSoftShadowMap;

/**
 * Animate
 *
 * Note here that anaverse doesn't support room animation.
 *
 */
const clock = new THREE.Clock();

const tick = () => {
  const elapsedTime = clock.getElapsedTime();

  // // animate the sphere
  // sphere.position.x = Math.cos(elapsedTime);
  // sphere.position.z = 1 + Math.sin(elapsedTime);
  // sphere.position.y = Math.abs(Math.sin(elapsedTime * 3));

  // Update controls
  controls.update();

  // Render
  renderer.render(scene, camera);

  // Call tick again on the next frame
  window.requestAnimationFrame(tick);
};

tick();